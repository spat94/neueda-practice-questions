package com.example;



public class PracticeMain {

	public static void main(String[] args) {
		
		
		for(int i = 1; i<=12; i ++)
		{
			for(int j = 1; j<=12; j++) //for loop nested in for loop, both starting at 1 and incrementing by 1 to 12
			{
			System.out.println(i + "x" + j + "=" + i*j); //prints output of each for loop and multiplies output to give product of each 
			}
		
		}
	}

}
